# Словари
# Пустой словарь
# 1. d = {}
# 2. d = dict()
# Предзаполненный словарь
d = {
    'table': ['таблица', 'стол'],
    'chair': ['стул', 'колодец'],
    'well' : ['хорошо'],
    'apple': 'яблоко',
    (55.75, 37.5): 'Москва',
}

print(d['table'][0])
if type(d['well']) == list:
    d['well'].append('скважина')
d['plum'] = 'слива'
print(d)

# перебрать словарь по ключам ( по умолчанию)

for key in d:
    print(key, '->', d[key])

print(list(d.keys()))  # список всех ключей

# перебрать все значения
for value in d.values():
    print(value)

print(list(d.values()))  # список всех значений в виде кортежей

print(d.items())  # список пар - ключ+значение

# перебор всех значений (пара ключ+значение)
for k, v in d.items():
    print(k, '->', v)


print('Если стул в словаре')
if 'стул' in d.values():
    print('Да, есть')

# доступ к несуществующему ключу без 'исключений'
# метод GET - мягкое обращение по ключу, возвращает результат (либо None, либо значение по умолчанию)
pear = d.get('pear', 'Груши тут нет')
print('Где груша: ', pear)


