# частотный анализ
text = '''Американский лидер Дональд Трамп резко ответил предпринимателю
          Илону Маску на критику нового налогового законопроекта, заявив,
          что без государственной поддержки бизнесмену придётся покинуть США.
          Трамп также заявил, что без запусков ракет, спутников и 
          производства электрокаров США могли бы сэкономить значительные 
          средства. США, по ожиданиям аналитиков, могут оказаться на грани
         дефолта к августу, если республиканцы и демократы в конгрессе не
          договорятся о повышении потолка госдолга. Маск уже грозил 
          создать новую партию после своей первой ссоры с Трампом в 
          начале июня.'''

commas = ('.', ',', '?', '!', ':', ';')
for x in commas:
    text = text.replace(x, '')

lst_text = sorted(text.strip().lower().split())

dic_words = {}

for word in lst_text:
    if word in dic_words:
        dic_words[word] += 1
    else:
        dic_words[word] = 1

print('Частотный анализ слов текста: ')
for k, v in dic_words.items():
    print(f'\t{k}:{v}')
